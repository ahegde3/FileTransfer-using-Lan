class fileclient {

    public void send(String a, String b) throws IOException {
        int i;
        FileInputStream fis = new FileInputStream(a);

        Socket sock = new Socket(b, 6666);
        DataOutputStream os = new DataOutputStream(sock.getOutputStream());
        while ((i = fis.read()) != -1) {
            os.write(i);
        }

        fis.close();
        os.close();
        sock.close();
    }
}

