class MYSERVER {

    public void recieve() {
        try {
            ServerSocket ss = new ServerSocket(6666);
            Socket s = ss.accept();//establishes connection   
            DataInputStream dis = new DataInputStream(s.getInputStream());
            FileOutputStream b = new FileOutputStream("D:\\new.txt");
            int i;
            System.out.println("Connecton establised");

            while ((i = dis.read()) != -1) {
                b.write(i);
                System.out.print("/");
            }
            System.out.println("Transfer complete");
            b.close();
            ss.close();
        } catch (Exception e) {
            System.out.println(e);
        }
    }
}

